package com.loiane.cursojava.aula33;

public class MinhaCalculadora {
    public int somar(int numerooUm, int numeroDois) {
        return numerooUm + numeroDois;
    }
    public int somar(int numeroUm, int numeroDois, int numeroTres) {
        return numeroUm + numeroDois + numeroTres;
    }
    public double somar(double numeroUm, double numeroDois) {
        return numeroUm + numeroDois;
    }
}
